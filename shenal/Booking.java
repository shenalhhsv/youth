    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed

        try {
            String place = String.valueOf(jComboBox1.getSelectedItem());
            String nic = jTextField1.getText();
            String firstName = jTextField2.getText();
            String lastName = jTextField3.getText();
            String mobile = jTextField4.getText();
            String date = jTextField5.getText();
            String time = jTextField6.getText();

            if (place.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Enter A Place", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (nic.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The NIC", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (firstName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Your Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (lastName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (mobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter A Mobile No.", "Warning", JOptionPane.WARNING_MESSAGE);
//            }else if (mobile.matches("^07[1245678]{1}[0-9]{7}$")) {
//                JOptionPane.showMessageDialog(this, "Please Enter Valid Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (date.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The Date", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (time.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The Time", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                MySQL.executeIUD("INSERT INTO `booking`(`place_id`,`nic`,`fname`,`lname`,`mobile`,`date`,`time`)"
                        + "VALUES('" + placeMap.get(place) + "','" + nic + "','" + firstName + "','" + lastName + "','" + mobile + "',"
                        + "'" + date + "','" + time + "')");
                reset();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton15ActionPerformed

