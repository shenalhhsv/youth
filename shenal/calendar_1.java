    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            String title = jTextField1.getText();
            String time = jTextField2.getText();
            String date = jDateChooser1.getDateFormatString();
            String details = jTextField3.getText();
            String customer = String.valueOf(jComboBox1.getSelectedItem());

            if (title.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter A Title", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (time.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The Time", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (date.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The Date", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (details.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Details", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (customer.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Enter A Customer", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                MySQL.executeIUD("INSERT INTO `events`(`event_name`,`time`,`dates`,`details`,`customers_id`)"
                        + "VALUES('" + title + "','" + time + "','" + date + "',"
                        + "'" + details + "','" + customerMap.get(customer) + "')");
                reset();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        

        String title = String.valueOf(jTable1.getValueAt(row, 1));
        jTextField1.setText(title);

        String time = String.valueOf(jTable1.getValueAt(row, 2));
        jTextField2.setText(time);

        String date = String.valueOf(jTable1.getValueAt(row, 3));
        jDateChooser1.setDateFormatString(date);
        
        String details = String.valueOf(jTable1.getValueAt(row, 3));
        jTextField3.setText(details);
        
        String customer  = String.valueOf(jTable1.getValueAt(row, 0));
        jComboBox1.setSelectedItem(customer);

        
    }//GEN-LAST:event_jTable1MouseClicked

