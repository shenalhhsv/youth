    private void loadCustomer() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customers` ");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("first_name"));
                customerMap.put(resultSet.getString("first_name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    private void loadBooking() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `events`"
                    + " INNER JOIN `customers` ON `events`.`customers_id`= `customers`.`id` ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("title"));
                vector.add(resultSet.getString("time"));
                vector.add(resultSet.getString("date"));
                vector.add(resultSet.getString("details"));
                vector.add(resultSet.getString("customers.name"));

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

